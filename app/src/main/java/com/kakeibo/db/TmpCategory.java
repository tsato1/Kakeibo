package com.kakeibo.db;

public class TmpCategory {
    public int code;
    public int color;
    public int drawable;
    public int location;
    public String ara;
    public String eng;
    public String spa;
    public String fra;
    public String hin;
    public String ind;
    public String ita;
    public String jpn;
    public String kor;
    public String pol;
    public String por;
    public String rus;
    public String tur;
    public String hans;
    public String hant;

    public TmpCategory() {}

    public TmpCategory(int code, int color, int drawable, int location,
                       String ara, String eng, String spa, String fra, String hin, String ind,
                       String ita, String jpn, String kor, String pol, String por, String rus,
                       String tur, String hans, String hant) {
        this.code = code;
        this.color = color;
        this.drawable = drawable;
        this.location = location;
        this.ara = ara;
        this.eng = eng;
        this.spa = spa;
        this.fra = fra;
        this.hin = hin;
        this.ind = ind;
        this.ita = ita;
        this.jpn = jpn;
        this.kor = kor;
        this.pol = pol;
        this.por = por;
        this.rus = rus;
        this.tur = tur;
        this.hans = hans;
        this.hant = hant;
    }
}
